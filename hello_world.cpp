#include <iostream>
using namespace std;

int main() {
  cout << "Hello World! \n";
  cout << "I'm learning C++! \n\n";
  cout << "I like coding." << endl;
  cout << "It is very fun. \t";
  cout << "Programming is very interesting. \\";
  cout << "It's my \"hobby\".";
  return 0;
}

